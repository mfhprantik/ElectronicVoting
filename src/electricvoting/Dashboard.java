    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        update.setEnabled(false);

        if (fname.getText().length() == 0 || lname.getText().length() == 0 || email2.getText().length() == 0 || password.getText().length() == 0 || cpassword.getText().length() == 0) {
            JOptionPane.showMessageDialog(new JFrame(), "Mandatory fields cannot be left empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (duplicateEmail()) {
            JOptionPane.showMessageDialog(new JFrame(), "An acount associated with your email already exists.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (password.getText().length() < 8 || password.getText().length() > 32) {
            JOptionPane.showMessageDialog(new JFrame(), "Password should be between 8 to 32 characters.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!password.getText().equals(cpassword.getText())) {
            JOptionPane.showMessageDialog(new JFrame(), "Password do not match confirmed password.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            SQL.updateProfile(fname.getText(), lname.getText(), email2.getText(), password.getText());
        }

        update.setEnabled(true);
    }//GEN-LAST:event_updateActionPerformed

    private void search2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search2ActionPerformed
        search2.setEnabled(false);

        if (pid.getText().length() == 0) {
            JOptionPane.showMessageDialog(new JFrame(), "Poll id field cannot be left empty.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (pid.getText().length() > 11) {
            JOptionPane.showMessageDialog(new JFrame(), "Invalid poll id.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!pid.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(new JFrame(), "Poll id field can only contain numeric values", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            ArrayList<Poll> polls = SQL.getPolls(SQL.SEARCH_POLLS + pid.getText());
            if (polls.size() == 0) {
                JOptionPane.showMessageDialog(new JFrame(), "No polls found.", "Info", JOptionPane.INFORMATION_MESSAGE);
            } else {
                Poll p = polls.get(0);

                if (p.isProtection() && !p.getCreator().equals(email)) {
                    new Password(p, this).setVisible(true);
                } else {
                    new ViewPoll(p).setVisible(true);
                    this.setVisible(false);
                }
            }
        }

        search2.setEnabled(true);
    }//GEN-LAST:event_search2ActionPerformed

