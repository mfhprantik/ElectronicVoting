    private String getPolls() {
        StringBuilder polls = SQL.getMyVotedPolls();

        if (polls.length() == 0) {
            return "0";
        }

        return polls.toString();
    }

    private void loadPolls(String pollQuery, JPanel tab, int startY) {
        ArrayList<Poll> polls = SQL.getPolls(pollQuery);

        JLabel columnHeaders = new JLabel("ID              Date Created              Title");
        columnHeaders.setFont(new Font(welcome.getFont().getName(), Font.BOLD, welcome.getFont().getSize()));
        columnHeaders.setForeground(Color.WHITE);
        columnHeaders.setBackground(new Color(0, 188, 212));
        columnHeaders.setBounds(100, startY, 700, 50);

        JSeparator headerSeparator = new JSeparator();
        headerSeparator.setForeground(Color.white);
        headerSeparator.setBounds(75, startY + 50, 750, 5);
        tab.add(columnHeaders);
        tab.add(headerSeparator);

        for (int i = 0, y = startY + 60; i < polls.size(); i++, y += 45) {
            Poll p = polls.get(i);

            JLabel b = new JLabel(getRow(p));
            b.setFont(welcome.getFont());
            b.setBackground(new Color(0, 188, 212));
            b.setForeground(Color.WHITE);
            b.setBounds(100, y, 700, 50);
            tab.add(b);
            JFrame jf = this;

            b.addMouseListener(new MouseListener() {
                @Override
                public void mouseClicked(MouseEvent e) {
                }

                @Override
                public void mousePressed(MouseEvent e) {
                    if (p.isProtection() && !p.getCreator().equals(email)) {
                        new Password(p, jf).setVisible(true);
                    } else {
                        new ViewPoll(p).setVisible(true);
                        jf.setVisible(false);
                    }
                }

                @Override
                public void mouseReleased(MouseEvent e) {
                }

                @Override
                public void mouseEntered(MouseEvent e) {
                }

                @Override
                public void mouseExited(MouseEvent e) {
                }
            });
        }
    }

